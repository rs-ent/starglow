"use client";

import { useState, useMemo } from "react";
import { usePlayerAssetSet } from "@/app/actions/playerAssets/hooks";
import { useUserGet } from "@/app/hooks/useUser";
import { useToast } from "@/app/hooks/useToast";
import FileUploader from "@/components/atoms/FileUploader";
import { cn } from "@/lib/utils/tailwind";

import type { UserWithPlayer } from "@/app/actions/user";

const GAME_MONEY_ASSET_ID = "cmcq98cyn00vpjt0vlxsb9esn";

export default function AdminDataMigrationsSGP() {
    const [csvData, setCsvData] = useState<any[]>([]);
    const [currentPage, setCurrentPage] = useState(1);
    const [itemsPerPage] = useState(50);

    const [isMigratingSGP, setIsMigratingSGP] = useState(false);
    const [sgpMigrationProgress, setSgpMigrationProgress] = useState(0);
    const [migratedSGP, setMigratedSGP] = useState<Set<number>>(new Set());
    const [migrationLog, setMigrationLog] = useState<string[]>([]);

    const { users } = useUserGet({
        getUsersInput: { providers: ["telegram"] },
    });

    // üöÄ ÏÑ±Îä• ÏµúÏ†ÅÌôî: ÏÇ¨Ïö©Ïûê Ï°∞ÌöåÎ•º MapÏúºÎ°ú Î≥ÄÌôò
    const usersMap = useMemo(() => {
        if (!users) return new Map();
        const map = new Map<string, UserWithPlayer>();
        users.forEach((user: UserWithPlayer) => {
            if (user.telegramId) {
                map.set(user.telegramId, user);
            }
        });
        return map;
    }, [users]);

    const { updatePlayerAsset } = usePlayerAssetSet();
    const toast = useToast();

    // Game Money Î≥ÄÌôò Ìï®Ïàò
    const convertGameMoney = (gameMoney: string | number): number => {
        const original = parseInt(String(gameMoney)) || 0;
        const converted = original / 1000;
        return Math.ceil(converted);
    };

    // CSV ÌååÏùº ÏóÖÎ°úÎìú Ï≤òÎ¶¨
    const handleFileUpload = async (files: { id: string; url: string }[]) => {
        if (files.length === 0) return;

        try {
            const response = await fetch(files[0].url);
            const text = await response.text();
            const rows = text.split("\n");
            const headers = rows[0].split(",").map((header) => header.trim());

            const parsedData = rows.slice(1).map((row) => {
                const values = row.split(",").map((value) => value.trim());
                return headers.reduce((obj, header, index) => {
                    obj[header] = values[index] || "";
                    return obj;
                }, {} as Record<string, string>);
            });

            setCsvData(parsedData);
            toast.success("CSV file loaded successfully");
        } catch (error) {
            console.error("Error parsing CSV:", error);
            toast.error("Failed to parse CSV file");
        }
    };

    // üîÑ Î°úÍ∑∏ Ï∂îÍ∞Ä Ìï®Ïàò
    const addLog = (message: string) => {
        const timestamp = new Date().toISOString().slice(11, 19);
        setMigrationLog((prev) => [...prev, `[${timestamp}] ${message}`]);
    };

    // ‚úÖ Í∞úÎ≥Ñ SGP ÎßàÏù¥Í∑∏Î†àÏù¥ÏÖò - ÏïàÏ†ÑÏÑ± Í∞ïÌôî
    const handleMigrateSGP = async (playerData: any, index?: number) => {
        const playerName = playerData["Name"] || "Unknown";
        const snsId = playerData["SNS ID"] || "";

        try {
            // üîç Í∏∞Î≥∏ Îç∞Ïù¥ÌÑ∞ Í≤ÄÏ¶ù
            if (!snsId || !playerData["Game Money"]) {
                addLog(`‚ùå ${playerName}: Missing SNS ID or Game Money`);
                return {
                    success: false,
                    skipped: true,
                    reason: "Missing required data",
                };
            }

            // üîç TelegramId Ï∂îÏ∂ú Î∞è Í≤ÄÏ¶ù
            if (!snsId.startsWith("TG_")) {
                addLog(`‚ùå ${playerName}: Invalid SNS ID format: ${snsId}`);
                return {
                    success: false,
                    skipped: true,
                    reason: "Invalid SNS ID format",
                };
            }

            const telegramId = snsId.replace("TG_", "");
            const user = usersMap.get(telegramId);

            if (!user?.player?.id) {
                addLog(
                    `‚ùå ${playerName}: User not found for telegramId: ${telegramId}`
                );
                return {
                    success: false,
                    error: "Player not found",
                    reason: "User not found",
                };
            }

            // üîç Game Money Î≥ÄÌôò Î∞è Í≤ÄÏ¶ù
            const originalGameMoney = parseInt(playerData["Game Money"]) || 0;
            const convertedAmount = convertGameMoney(originalGameMoney);

            if (originalGameMoney < 0) {
                addLog(
                    `‚ùå ${playerName}: Negative Game Money: ${originalGameMoney}`
                );
                return {
                    success: false,
                    error: "Invalid Game Money",
                    reason: "Negative amount",
                };
            }

            if (convertedAmount <= 0) {
                addLog(
                    `‚è≠Ô∏è ${playerName}: Zero SGP (${originalGameMoney} ‚Üí ${convertedAmount}), skipping`
                );
                return { success: true, skipped: true, reason: "Zero amount" };
            }

            // üîç Î≥ÄÌôò Í≤∞Í≥º Î°úÍπÖ
            addLog(
                `üîÑ ${playerName}: Converting ${originalGameMoney} ‚Üí ${convertedAmount} SGP`
            );

            // üö® Ï§ëÏöî: ADD Ïó∞ÏÇ∞ÏúºÎ°ú Î≥ÄÍ≤ΩÌïòÏó¨ Í∏∞Ï°¥ ÏûîÏï° Î≥¥Ï°¥
            const result = await updatePlayerAsset({
                transaction: {
                    playerId: user.player.id,
                    assetId: GAME_MONEY_ASSET_ID,
                    amount: convertedAmount,
                    operation: "SET",
                    reason: "Migration from MEME QUEST - Game Money conversion (1/1000, rounded up)",
                    metadata: {
                        source: "csv_migration",
                        originalAmount: originalGameMoney,
                        convertedAmount: convertedAmount,
                        conversionRate: 0.001,
                        rounded: true,
                        playerName: playerName,
                        telegramId: telegramId,
                        migrationTimestamp: new Date().toISOString(),
                    },
                },
            });

            if (result.success) {
                addLog(
                    `‚úÖ ${playerName}: Successfully added ${convertedAmount} SGP`
                );

                if (index !== undefined) {
                    setMigratedSGP((prev) => {
                        const newSet = new Set(prev);
                        newSet.add(index);
                        return newSet;
                    });
                }
            } else {
                addLog(
                    `‚ùå ${playerName}: Failed to update asset: ${result.error}`
                );
            }

            return {
                success: result.success,
                result,
                playerName,
                originalAmount: originalGameMoney,
                convertedAmount: convertedAmount,
            };
        } catch (error) {
            const errorMessage =
                error instanceof Error ? error.message : String(error);
            addLog(`üí• ${playerName}: Exception occurred: ${errorMessage}`);
            console.error("SGP migration failed:", {
                playerName,
                snsId,
                error,
            });
            return {
                success: false,
                error: errorMessage,
                playerName,
                reason: "Exception occurred",
            };
        }
    };

    // üöÄ ÏùºÍ¥Ñ SGP ÎßàÏù¥Í∑∏Î†àÏù¥ÏÖò - ÏÑ±Îä• Î∞è ÏïàÏ†ÑÏÑ± Í∞ïÌôî
    const handleMigrateAllSGP = async () => {
        setIsMigratingSGP(true);
        setSgpMigrationProgress(0);
        setMigrationLog([]); // Î°úÍ∑∏ Ï¥àÍ∏∞Ìôî

        const batchSize = 25; // üöÄ Î∞∞Ïπò ÌÅ¨Í∏∞ Ï¶ùÍ∞Ä (10 ‚Üí 25)
        let successCount = 0;
        let failCount = 0;
        let skipCount = 0;
        let totalSGPAdded = 0;
        let totalGameMoneyProcessed = 0;

        const startTime = Date.now();
        addLog(`üöÄ Starting SGP migration for ${csvData.length} players...`);
        addLog(
            `üìä Batch size: ${batchSize}, Total batches: ${Math.ceil(
                csvData.length / batchSize
            )}`
        );

        try {
            for (let i = 0; i < csvData.length; i += batchSize) {
                const batch = csvData.slice(i, i + batchSize);
                const batchNumber = Math.floor(i / batchSize) + 1;
                const totalBatches = Math.ceil(csvData.length / batchSize);

                addLog(
                    `üì¶ Processing batch ${batchNumber}/${totalBatches} (${batch.length} players)...`
                );

                const batchResults = await Promise.all(
                    batch.map((playerData, idx) =>
                        handleMigrateSGP(playerData, i + idx)
                    )
                );

                // üî¢ Î∞∞Ïπò Í≤∞Í≥º ÏßëÍ≥Ñ
                let batchSuccess = 0;
                let batchFail = 0;
                let batchSkip = 0;
                let batchSGP = 0;

                batchResults.forEach((result) => {
                    if (result.success && !result.skipped) {
                        batchSuccess++;
                        successCount++;
                        if (result.convertedAmount) {
                            batchSGP += result.convertedAmount;
                            totalSGPAdded += result.convertedAmount;
                        }
                        if (result.originalAmount) {
                            totalGameMoneyProcessed += result.originalAmount;
                        }
                    } else if (result.skipped) {
                        batchSkip++;
                        skipCount++;
                    } else {
                        batchFail++;
                        failCount++;
                    }
                });

                addLog(
                    `‚úÖ Batch ${batchNumber} completed: ${batchSuccess} success, ${batchSkip} skipped, ${batchFail} failed, ${batchSGP} SGP added`
                );

                // üîÑ ÏßÑÌñâÎ•† ÏóÖÎç∞Ïù¥Ìä∏
                const progress = Math.floor(
                    ((i + batch.length) / csvData.length) * 100
                );
                setSgpMigrationProgress(progress);

                // üöÄ ÏßÄÏó∞ ÏãúÍ∞Ñ ÏµúÏ†ÅÌôî (100ms ‚Üí 50ms)
                await new Promise((resolve) => setTimeout(resolve, 50));
            }

            const endTime = Date.now();
            const duration = ((endTime - startTime) / 1000).toFixed(2);

            addLog(`üéâ Migration completed in ${duration}s`);
            addLog(
                `üìä Final results: ${successCount} successful, ${skipCount} skipped, ${failCount} failed`
            );
            addLog(`üí∞ Total SGP added: ${totalSGPAdded.toLocaleString()}`);
            addLog(
                `üéÆ Total Game Money processed: ${totalGameMoneyProcessed.toLocaleString()}`
            );

            toast.success(
                `SGP Migration completed: ${successCount} successful, ${skipCount} skipped, ${failCount} failed. Total SGP added: ${totalSGPAdded.toLocaleString()}`
            );
        } catch (error) {
            const errorMessage =
                error instanceof Error ? error.message : String(error);
            addLog(`üí• Critical error: ${errorMessage}`);
            console.error("Batch SGP migration failed:", error);
            toast.error(
                `SGP migration process encountered an error: ${errorMessage}`
            );
        } finally {
            setIsMigratingSGP(false);
            addLog(`‚èπÔ∏è Migration process ended`);
        }
    };

    const totalPages = Math.ceil(csvData.length / itemsPerPage);
    const startIndex = (currentPage - 1) * itemsPerPage;
    const paginatedData = csvData.slice(startIndex, startIndex + itemsPerPage);

    return (
        <div className="container mx-auto py-8 px-4">
            <h1 className="text-2xl font-bold mb-6">
                üí∞ SGP Migration from Game Money
            </h1>

            <div className="mb-4 p-4 bg-blue-50 dark:bg-blue-900/20 rounded-lg">
                <h3 className="font-semibold text-blue-900 dark:text-blue-100 mb-2">
                    üîê ÏïàÏ†ÑÌïú Î≥ÄÌôò Í∑úÏπô
                </h3>
                <ul className="text-sm text-blue-800 dark:text-blue-200 space-y-1">
                    <li>
                        ‚Ä¢ <strong>Î≥ÄÌôò Í≥µÏãù:</strong> Game Money √∑ 1000 = SGP
                        (ÏÜåÏà´Ï†ê Ïò¨Î¶º)
                    </li>
                    <li>
                        ‚Ä¢ <strong>ÏòàÏãú:</strong> 13,200 ‚Üí 14 SGP, 800 ‚Üí 1 SGP
                    </li>
                    <li>
                        ‚Ä¢ <strong>Ïó∞ÏÇ∞ Î∞©Ïãù:</strong> ADD (Í∏∞Ï°¥ SGP ÏûîÏï°Ïóê Ï∂îÍ∞Ä)
                    </li>
                    <li>
                        ‚Ä¢ <strong>Ï§ëÎ≥µ Î∞©ÏßÄ:</strong> ÎèôÏùº ÏÇ¨Ïö©Ïûê Ïû¨Ïã§Ìñâ Ïãú Ï§ëÎ≥µ
                        Ï∂îÍ∞ÄÎê®
                    </li>
                    <li>
                        ‚Ä¢ <strong>Asset ID:</strong> {GAME_MONEY_ASSET_ID}
                    </li>
                </ul>
            </div>

            <div className="mb-4 p-4 bg-amber-50 dark:bg-amber-900/20 rounded-lg border border-amber-200 dark:border-amber-800">
                <h3 className="font-semibold text-amber-900 dark:text-amber-100 mb-2">
                    ‚ö†Ô∏è Ï§ëÏöî Ï£ºÏùòÏÇ¨Ìï≠
                </h3>
                <ul className="text-sm text-amber-800 dark:text-amber-200 space-y-1">
                    <li>
                        ‚Ä¢ Ïù¥ ÎßàÏù¥Í∑∏Î†àÏù¥ÏÖòÏùÄ <strong>Ìïú Î≤àÎßå</strong> Ïã§ÌñâÌï¥Ïïº
                        Ìï©ÎãàÎã§
                    </li>
                    <li>
                        ‚Ä¢ Ïû¨Ïã§Ìñâ Ïãú SGPÍ∞Ä <strong>Ï§ëÎ≥µÏúºÎ°ú Ï∂îÍ∞Ä</strong>Îê©ÎãàÎã§
                    </li>
                    <li>‚Ä¢ Î∞∞Ïπò ÌÅ¨Í∏∞: 25Í∞úÏî© Ï≤òÎ¶¨ÌïòÏó¨ ÏÑ±Îä• ÏµúÏ†ÅÌôî</li>
                    <li>‚Ä¢ Ïã§ÏãúÍ∞Ñ Î°úÍ∑∏Î°ú Î™®Îì† Í≥ºÏ†ïÏùÑ Ï∂îÏ†Å Í∞ÄÎä•</li>
                </ul>
            </div>

            <div className="mb-8">
                <h2 className="text-xl font-semibold mb-4">Upload CSV File</h2>
                <FileUploader
                    bucket="data-migrations"
                    purpose="sgp-migration"
                    onComplete={handleFileUpload}
                    accept={{ "text/csv": [".csv"] }}
                    multiple={false}
                    className="max-w-2xl"
                />
            </div>

            {csvData.length > 0 && (
                <div className="mt-8">
                    <h2 className="text-xl font-semibold mb-4">
                        SGP Migration Data
                    </h2>

                    {/* ÌÖåÏù¥Î∏îÍ≥º ÌéòÏù¥ÏßÄÎÑ§Ïù¥ÏÖò */}
                    <div className="overflow-auto max-h-[500px]">
                        <table className="min-w-full border border-[rgba(255,255,255,0.5)]">
                            <thead>
                                <tr className="bg-[rgba(255,255,255,0.3)]">
                                    <th className="border-b text-center text-sm font-bold">
                                        Name
                                    </th>
                                    <th className="border-b text-center text-sm font-bold">
                                        SNS ID
                                    </th>
                                    <th className="border-b text-center text-sm font-bold">
                                        Game Money
                                    </th>
                                    <th className="border-b text-center text-sm font-bold">
                                        ‚Üí SGP
                                    </th>
                                    <th className="border-b text-center text-sm font-bold">
                                        Status
                                    </th>
                                </tr>
                            </thead>
                            <tbody>
                                {paginatedData.map((row, rowIndex) => {
                                    const originalGameMoney =
                                        parseInt(row["Game Money"]) || 0;
                                    const convertedSGP =
                                        convertGameMoney(originalGameMoney);
                                    const realIndex = startIndex + rowIndex;

                                    return (
                                        <tr
                                            key={rowIndex}
                                            className={cn(
                                                "divide-x divide-[rgba(255,255,255,0.1)]",
                                                rowIndex % 2 === 0
                                                    ? "bg-[rgba(255,255,255,0.1)]"
                                                    : "bg-[rgba(255,255,255,0.2)]",
                                                migratedSGP.has(realIndex) &&
                                                    "bg-[rgba(0,255,0,0.1)]"
                                            )}
                                        >
                                            <td className="py-2 px-4 text-xs">
                                                {row["Name"]}
                                            </td>
                                            <td className="py-2 px-4 text-xs">
                                                {row["SNS ID"]}
                                            </td>
                                            <td className="py-2 px-4 text-xs text-right">
                                                {originalGameMoney.toLocaleString()}
                                            </td>
                                            <td className="py-2 px-4 text-xs text-right font-bold text-green-400">
                                                {convertedSGP}
                                            </td>
                                            <td className="py-2 px-4 text-xs text-center">
                                                {migratedSGP.has(realIndex) ? (
                                                    <span className="text-green-400">
                                                        ‚úì Migrated
                                                    </span>
                                                ) : (
                                                    <span className="text-yellow-400">
                                                        Pending
                                                    </span>
                                                )}
                                            </td>
                                        </tr>
                                    );
                                })}
                            </tbody>
                        </table>
                    </div>

                    {/* ÌéòÏù¥ÏßÄÎÑ§Ïù¥ÏÖò */}
                    <div className="mt-4 flex items-center justify-between">
                        <div>
                            <span className="text-sm text-gray-700">
                                Showing {startIndex + 1} to{" "}
                                {Math.min(
                                    startIndex + itemsPerPage,
                                    csvData.length
                                )}{" "}
                                of {csvData.length} entries
                            </span>
                        </div>
                        <div className="flex space-x-2">
                            <button
                                onClick={() =>
                                    setCurrentPage((prev) =>
                                        Math.max(prev - 1, 1)
                                    )
                                }
                                disabled={currentPage === 1}
                                className={`px-3 py-1 text-xs rounded border ${
                                    currentPage === 1
                                        ? "bg-[rgba(255,255,255,0.1)] text-[rgba(255,255,255,0.3)] cursor-not-allowed"
                                        : "bg-[rgba(255,255,255,0.2)] hover:bg-[rgba(255,255,255,0.3)]"
                                }`}
                            >
                                Previous
                            </button>

                            {(() => {
                                const pageGroup = Math.floor(
                                    (currentPage - 1) / 10
                                );
                                const startPage = pageGroup * 10 + 1;
                                const endPage = Math.min(
                                    startPage + 9,
                                    totalPages
                                );

                                const pageButtons = [];
                                for (let i = startPage; i <= endPage; i++) {
                                    pageButtons.push(
                                        <button
                                            key={i}
                                            onClick={() => setCurrentPage(i)}
                                            className={`px-3 py-1 text-xs rounded border ${
                                                currentPage === i
                                                    ? "bg-[rgba(255,255,255,0.9)] text-[rgba(0,0,0,0.9)]"
                                                    : "bg-[rgba(255,255,255,0.2)] hover:bg-[rgba(255,255,255,0.3)]"
                                            }`}
                                        >
                                            {i}
                                        </button>
                                    );
                                }
                                return pageButtons;
                            })()}

                            <button
                                onClick={() =>
                                    setCurrentPage((prev) =>
                                        Math.min(prev + 1, totalPages)
                                    )
                                }
                                disabled={currentPage === totalPages}
                                className={`px-3 py-1 rounded text-xs border ${
                                    currentPage === totalPages
                                        ? "bg-[rgba(255,255,255,0.1)] text-[rgba(255,255,255,0.3)] cursor-not-allowed"
                                        : "bg-[rgba(255,255,255,0.2)] hover:bg-[rgba(255,255,255,0.3)]"
                                }`}
                            >
                                Next
                            </button>
                        </div>
                    </div>

                    {/* ÏùºÍ¥Ñ ÎßàÏù¥Í∑∏Î†àÏù¥ÏÖò Î≤ÑÌäº */}
                    <div className="my-6">
                        <div className="flex items-center gap-4">
                            <button
                                onClick={handleMigrateAllSGP}
                                disabled={isMigratingSGP}
                                className={cn(
                                    "py-2 px-4 text-white rounded transition-colors",
                                    isMigratingSGP
                                        ? "bg-green-400 cursor-not-allowed"
                                        : "bg-green-600 hover:bg-green-700"
                                )}
                            >
                                {isMigratingSGP
                                    ? "Migrating SGP..."
                                    : "Migrate All SGP"}
                            </button>

                            {isMigratingSGP && (
                                <div className="flex-1">
                                    <div className="w-full bg-gray-700 rounded-full h-2.5">
                                        <div
                                            className="bg-green-600 h-2.5 rounded-full"
                                            style={{
                                                width: `${sgpMigrationProgress}%`,
                                            }}
                                        ></div>
                                    </div>
                                    <div className="text-xs mt-1 text-gray-400">
                                        {sgpMigrationProgress}% Complete
                                    </div>
                                </div>
                            )}
                        </div>
                    </div>

                    {/* üîç Ïã§ÏãúÍ∞Ñ ÎßàÏù¥Í∑∏Î†àÏù¥ÏÖò Î°úÍ∑∏ */}
                    {migrationLog.length > 0 && (
                        <div className="my-6">
                            <h3 className="text-lg font-semibold mb-3">
                                üìã Migration Log
                            </h3>
                            <div className="bg-black/90 rounded-lg p-4 max-h-64 overflow-y-auto">
                                <div className="space-y-1 font-mono text-xs">
                                    {migrationLog
                                        .slice(-50)
                                        .map((log, index) => (
                                            <div
                                                key={index}
                                                className={cn(
                                                    "whitespace-pre-wrap",
                                                    log.includes("‚ùå") &&
                                                        "text-red-400",
                                                    log.includes("‚úÖ") &&
                                                        "text-green-400",
                                                    log.includes("‚è≠Ô∏è") &&
                                                        "text-yellow-400",
                                                    log.includes("üîÑ") &&
                                                        "text-blue-400",
                                                    log.includes("üì¶") &&
                                                        "text-purple-400",
                                                    log.includes("üöÄ") &&
                                                        "text-cyan-400",
                                                    log.includes("üéâ") &&
                                                        "text-green-300",
                                                    log.includes("üí•") &&
                                                        "text-red-500",
                                                    !log.includes("‚ùå") &&
                                                        !log.includes("‚úÖ") &&
                                                        !log.includes("‚è≠Ô∏è") &&
                                                        !log.includes("üîÑ") &&
                                                        !log.includes("üì¶") &&
                                                        !log.includes("üöÄ") &&
                                                        !log.includes("üéâ") &&
                                                        !log.includes("üí•") &&
                                                        "text-gray-300"
                                                )}
                                            >
                                                {log}
                                            </div>
                                        ))}
                                </div>
                                {migrationLog.length > 50 && (
                                    <div className="text-xs text-gray-500 mt-2">
                                        Showing last 50 of {migrationLog.length}{" "}
                                        log entries
                                    </div>
                                )}
                            </div>
                        </div>
                    )}
                </div>
            )}
        </div>
    );
}
